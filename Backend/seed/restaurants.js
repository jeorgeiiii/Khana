const seedDatabase = async () => {
    try {
        await mongoose.connect(process.env.MONGO_URI, {
            dbName: process.env.DB_NAME || 'zomoro_db'
        });
        console.log('✅ Connected to MongoDB');

        await Resturant.deleteMany({});
        console.log('✅ Cleared existing restaurants');

        const result = await Resturant.insertMany(restaurants);
        console.log(`✅ Added ${result.length} restaurants`);

        // Group by location and count
        const locationCount = {};
        result.forEach(r => {
            locationCount[r.location] = (locationCount[r.location] || 0) + 1;
        });

        console.log('\n📊 Restaurants by City:');
        Object.keys(locationCount).sort().forEach(loc => {
            console.log(`   ${loc}: ${locationCount[loc]} restaurants`);
        });

        console.log('\n🎉 Database seeded successfully with restaurants from all over India!');

    } catch (error) {
        console.error('❌ Error seeding database:', error);
    } finally {
        await mongoose.disconnect();
        console.log('\n👋 Disconnected from MongoDB');
    }
};

